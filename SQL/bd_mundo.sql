SET NAMES 'utf8' COLLATE 'utf8_general_ci';
CREATE DATABASE bd_mundo character SET utf8 COLLATE utf8_general_ci;
-- CREATE DATABASE IF NOT EXISTS bd_mundo;
USE bd_mundo;

CREATE TABLE continentes (
  id INT NOT NULL,
  continente VARCHAR(20) NOT NULL,
  --
  CONSTRAINT pk_continente PRIMARY KEY (continente),
  CONSTRAINT uq_id UNIQUE KEY (id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE gobiernos (
  id INT NOT NULL,
  gobierno VARCHAR(40) NOT NULL,
  --
  CONSTRAINT pk_gobierno PRIMARY KEY (gobierno),
  CONSTRAINT uq_id UNIQUE KEY (id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE idiomas (
  id INT NOT NULL,
  idioma VARCHAR(20) NOT NULL,
  --
  CONSTRAINT pk_idioma PRIMARY KEY (idioma),
  CONSTRAINT uq_id UNIQUE KEY (id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE paises (
  id INT NOT NULL,
  pais VARCHAR(35) NOT NULL,
  capital VARCHAR(20),
  moneda VARCHAR(30),
  continente_id INT,
  gobierno_id INT,
  poblacion INT,
  extension DOUBLE(15,5),
  posicion VARCHAR(20),
  existe BOOLEAN NOT NULL DEFAULT 1,
  costa INT DEFAULT 0,
  country VARCHAR(35),
  code VARCHAR(2),
  --
  CONSTRAINT pk_pais PRIMARY KEY (pais),
  CONSTRAINT uq_id UNIQUE KEY id (id),
  CONSTRAINT fk_continente FOREIGN KEY (continente_id) REFERENCES continentes(id),
  CONSTRAINT fk_gobierno FOREIGN KEY (gobierno_id) REFERENCES gobiernos(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE paises_idiomas (
  pais_id INT NOT NULL,
  idioma_id INT NOT NULL,
  hablantes INT,
  porcentaje DOUBLE(15,5),
  --
  CONSTRAINT pk_paisidioma PRIMARY KEY (pais_id,idioma_id),
  CONSTRAINT fk_pais FOREIGN KEY (pais_id) REFERENCES paises(id),
  CONSTRAINT fk_idioma FOREIGN KEY (idioma_id) REFERENCES idiomas(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE paises_vecinos (
  pais_id INT NOT NULL,
  vecino_id INT NOT NULL,
  kms_frontera INT DEFAULT 0,
  --
  CONSTRAINT pk_paisvecino PRIMARY KEY (pais_id,vecino_id),
  CONSTRAINT fk_pais2 FOREIGN KEY (pais_id) REFERENCES paises(id),
  CONSTRAINT fk_vecino FOREIGN KEY (vecino_id) REFERENCES paises(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

